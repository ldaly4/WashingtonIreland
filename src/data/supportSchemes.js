export function getSupportScheme(id) {
  return supportSchemes.find(scheme => scheme.id === id);
}

export function sourceFromScheme(id) {
  const scheme = getSupportScheme(id);
  return scheme ? { label: scheme.officialProvider, url: scheme.officialUrl } : null;
}
